public class Pair {
    private int index;
    private int value;
    
    // takes two int parameters 
    Pair(int index, int value) {
        this.index = index;
        this.value = value;
    }
    
    // accessor methods 
    public int getIndex(){
        return index;
    }
    
    public int getValue(){
        return value;
    }

}   
